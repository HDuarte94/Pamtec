<p style="text-align: center;">
    Nosso Objetivo<br>
    Disponibilizar para nossos clientes soluções em prestação de serviços na
    área de manutenção corretiva, preventiva e calibração de equipamentos 
    médicos e laboratoriais, disponibilizando Certificados de Calibração, 
    visando garantir a precisão e repetibilidade das grandezas medidas.
</p>

<p style="text-align: center;">
    Apresentação da Empresa<br>
    A empresa Pam Technology Comércio e Serviços Ltda., fundada em 1998 tem 
    como produto principal a prestação de serviços na área de manutenção e 
    calibração de equipamentos médios e laboratoriais, possui colaboradores 
    capacitados para realizar manutenção e calibração em equipamentos 
    eletroeletrônicos de diversos seguimentos, com a emissão de Laudos de 
    Calibração com rastreabilidade RBC/InMetro, necessários para 
    credenciamento em Órgãos Certificadores (ONA,ISO,Joint Commission entre 
    outros).
</p>

<p style="text-align: center;">
    Por que calibrar os equipamentos?<br>
    A ANVISA – Agência Nacional de Vigilância Sanitária, através do Grupo de
    Tecnovigilância e seus diversos Órgãos Fiscalizadores, efetua um 
    rigoroso controle sobre as condições nas quais são realizados exames 
    diagnósticos envolvendo equipamentos e dispositivos eletromédicos. A 
    infetividade dos resultados obtidos, pode ocasionar, entre outras 
    consequências, suspensão do credenciamento do Estabelecimento 
    Assistencial de Saúde, conforme a natureza dos procedimentos propostos.
</p>